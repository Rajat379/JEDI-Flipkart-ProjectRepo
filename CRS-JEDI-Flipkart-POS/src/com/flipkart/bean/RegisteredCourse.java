public class RegisteredCourse {
	
	
	  /** The student id. */
	  private int studentId;

	  /** The course id. */
	  private int courseId;

	  /** The grade id. */
	  private int gradeId;

	  /** The semester registration id. */
	  private int semesterRegistrationId;

	  /** The id. */
	  private int id;

	/**
	 * @return the studentId
	 */
	public int getStudentId() {
		return studentId;
	}

	/**
	 * @param studentId the studentId to set
	 */
	public void setStudentId(int studentId) {
		this.studentId = studentId;
	}

	/**
	 * @return the courseId
	 */
	public int getCourseId() {
		return courseId;
	}

	/**
	 * @param courseId the courseId to set
	 */
	public void setCourseId(int courseId) {
		this.courseId = courseId;
	}

	/**
	 * @return the gradeId
	 */
	public int getGradeId() {
		return gradeId;
	}

	/**
	 * @param gradeId the gradeId to set
	 */
	public void setGradeId(int gradeId) {
		this.gradeId = gradeId;
	}

	/**
	 * @return the semesterRegistrationId
	 */
	public int getSemesterRegistrationId() {
		return semesterRegistrationId;
	}

	/**
	 * @param semesterRegistrationId the semesterRegistrationId to set
	 */
	public void setSemesterRegistrationId(int semesterRegistrationId) {
		this.semesterRegistrationId = semesterRegistrationId;
	}

	/**
	 * @return the id
	 */
	public int getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(int id) {
		this.id = id;
	}
	  
	  

}
